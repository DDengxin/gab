package com.tengzhi.business.system.workflow.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author lqy 流程描述表的扩展表
 */
@Entity
@Table(name = "sys_workflow_link_table")
public class LinkTable {
	private String workflowTableId;
	private String workflowTableDescribe;
	private String workflowTableName;
	private String workflowNoteFieldName;
	private String workflowFlagFieldName;
	private String workflowWorkerIdFieldName;
	private String workflowUnitFieldName;

	@Id
	public String getWorkflowTableId() {
		return workflowTableId;
	}

	public void setWorkflowTableId(String workflowTableId) {
		this.workflowTableId = workflowTableId;
	}

	public String getWorkflowTableDescribe() {
		return workflowTableDescribe;
	}

	public void setWorkflowTableDescribe(String workflowTableDescribe) {
		this.workflowTableDescribe = workflowTableDescribe;
	}

	public String getWorkflowTableName() {
		return workflowTableName;
	}

	public void setWorkflowTableName(String workflowTableName) {
		this.workflowTableName = workflowTableName;
	}

	public String getWorkflowNoteFieldName() {
		return workflowNoteFieldName;
	}

	public void setWorkflowNoteFieldName(String workflowNoteFieldName) {
		this.workflowNoteFieldName = workflowNoteFieldName;
	}

	public String getWorkflowFlagFieldName() {
		return workflowFlagFieldName;
	}

	public void setWorkflowFlagFieldName(String workflowFlagFieldName) {
		this.workflowFlagFieldName = workflowFlagFieldName;
	}

	public String getWorkflowWorkerIdFieldName() {
		return workflowWorkerIdFieldName;
	}

	public void setWorkflowWorkerIdFieldName(String workflowWorkerIdFieldName) {
		this.workflowWorkerIdFieldName = workflowWorkerIdFieldName;
	}

	public String getWorkflowUnitFieldName() {
		return workflowUnitFieldName;
	}

	public void setWorkflowUnitFieldName(String workflowUnitFieldName) {
		this.workflowUnitFieldName = workflowUnitFieldName;
	}
}
