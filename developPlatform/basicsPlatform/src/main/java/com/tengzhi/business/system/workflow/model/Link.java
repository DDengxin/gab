package com.tengzhi.business.system.workflow.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author lqy 工作环节
 */
@Entity
@Table(name = "sys_workflow_link")
public class Link {
	private String id;
	private String associatedId;
	private String processId;
	private String linkName;
	private String transactorType;
	private String transactorId;
	private String transactor;
	private String transactorWay;
	private String processType;
	private String whetherToSkip;
	private String shape;
	private String size;
	private String x;
	private String y;
	private String color;

	@Id
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAssociatedId() {
		return associatedId;
	}

	public void setAssociatedId(String associatedId) {
		this.associatedId = associatedId;
	}

	public String getProcessId() {
		return processId;
	}

	public void setProcessId(String processId) {
		this.processId = processId;
	}

	public String getLinkName() {
		return linkName;
	}

	public void setLinkName(String linkName) {
		this.linkName = linkName;
	}

	public String getTransactorType() {
		return transactorType;
	}

	public void setTransactorType(String transactorType) {
		this.transactorType = transactorType;
	}

	public String getTransactorId() {
		return transactorId;
	}

	public void setTransactorId(String transactorId) {
		this.transactorId = transactorId;
	}

	public String getTransactor() {
		return transactor;
	}

	public void setTransactor(String transactor) {
		this.transactor = transactor;
	}

	public String getTransactorWay() {
		return transactorWay;
	}

	public void setTransactorWay(String transactorWay) {
		this.transactorWay = transactorWay;
	}

	public String getProcessType() {
		return processType;
	}

	public void setProcessType(String processType) {
		this.processType = processType;
	}

	public Link(String id, String associatedId, String processId, String linkName, String transactorType,
			String transactorId, String transactor, String transactorWay, String processType) {
		super();
		this.id = id;
		this.associatedId = associatedId;
		this.processId = processId;
		this.linkName = linkName;
		this.transactorType = transactorType;
		this.transactorId = transactorId;
		this.transactor = transactor;
		this.transactorWay = transactorWay;
		this.processType = processType;
	}

	public Link() {
		super();
		// TODO Auto-generated constructor stub
	}

	public String getShape() {
		return shape;
	}

	public void setShape(String shape) {
		this.shape = shape;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getWhetherToSkip() {
		return whetherToSkip;
	}

	public void setWhetherToSkip(String whetherToSkip) {
		this.whetherToSkip = whetherToSkip;
	}

}
