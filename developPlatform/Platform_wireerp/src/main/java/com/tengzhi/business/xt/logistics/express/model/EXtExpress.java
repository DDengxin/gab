package com.tengzhi.business.xt.logistics.express.model;

import com.fasterxml.jackson.annotation.JsonFormat;

import javax.persistence.*;
import java.util.Date;

@Entity
@Table(name="e_xt_express")
public class EXtExpress {

    @Id
    private String note;
    @Temporal(TemporalType.TIMESTAMP)
    @JsonFormat(pattern = "yyyy-MM-dd")
    private Date expressRq;
    private String expressMan;
    private String expressDept;
    private String dataCorp;
    private String expressAddressee;
    private String expressRecipients;
    private String expressType;
    private String expressCost;
    private String expressRecipientsSite;
    private String expressRecipientsTel;
    private String expressCourierNumber;
    private String expressRemarks;

    public String getNote() { return note; }

    public void setNote(String note) { this.note = note; }

    public Date getExpressRq() { return expressRq; }

    public void setExpressRq(Date expressRq) { this.expressRq = expressRq; }

    public String getExpressMan() { return expressMan; }

    public void setExpressMan(String expressMan) { this.expressMan = expressMan; }

    public String getExpressDept() { return expressDept; }

    public void setExpressDept(String expressDept) { this.expressDept = expressDept; }

    public String getDataCorp() { return dataCorp; }

    public void setDataCorp(String dataCorp) { this.dataCorp = dataCorp; }

    public String getExpressAddressee() { return expressAddressee; }

    public void setExpressAddressee(String expressAddressee) { this.expressAddressee = expressAddressee; }

    public String getExpressRecipients() { return expressRecipients; }

    public void setExpressRecipients(String expressRecipients) { this.expressRecipients = expressRecipients; }

    public String getExpressType() { return expressType; }

    public void setExpressType(String expressType) { this.expressType = expressType; }

    public String getExpressCost() { return expressCost; }

    public void setExpressCost(String expressCost) { this.expressCost = expressCost; }

    public String getExpressRecipientsSite() { return expressRecipientsSite; }

    public void setExpressRecipientsSite(String expressRecipientsSite) { this.expressRecipientsSite = expressRecipientsSite; }

    public String getExpressRecipientsTel() { return expressRecipientsTel; }

    public void setExpressRecipientsTel(String expressRecipientsTel) { this.expressRecipientsTel = expressRecipientsTel; }

    public String getExpressCourierNumber() { return expressCourierNumber; }

    public void setExpressCourierNumber(String expressCourierNumber) { this.expressCourierNumber = expressCourierNumber; }

    public String getExpressRemarks() { return expressRemarks; }

    public void setExpressRemarks(String expressRemarks) { this.expressRemarks = expressRemarks; }
}
