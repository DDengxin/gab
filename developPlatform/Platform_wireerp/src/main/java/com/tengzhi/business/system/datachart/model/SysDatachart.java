package com.tengzhi.business.system.datachart.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/**
 * @author 郑书
 * @create 2020-07-27
 */

@Entity
@Table(name = "sys_datachart")
public class SysDatachart{
 private String datachartId; //图表编号
 private String datachartName;//图表名称
 private String datachartModule;//图表模块
 private String datachartFile;//图表文件
 private String datachartSm;//图表说明
 private String datachartFlag;//启用状态
 private String corpId;//所属公司
 
 @Id
public String getDatachartId() {
	return datachartId;
}
public void setDatachartId(String datachartId) {
	this.datachartId = datachartId;
}
public String getDatachartName() {
	return datachartName;
}
public void setDatachartName(String datachartName) {
	this.datachartName = datachartName;
}
public String getDatachartModule() {
	return datachartModule;
}
public void setDatachartModule(String datachartModule) {
	this.datachartModule = datachartModule;
}
public String getDatachartFile() {
	return datachartFile;
}
public void setDatachartFile(String datachartFile) {
	this.datachartFile = datachartFile;
}
public String getDatachartSm() {
	return datachartSm;
}
public void setDatachartSm(String datachartSm) {
	this.datachartSm = datachartSm;
}
public String getDatachartFlag() {
	return datachartFlag;
}
public void setDatachartFlag(String datachartFlag) {
	this.datachartFlag = datachartFlag;
}
public String getCorpId() {
	return corpId;
}
public void setCorpId(String corpId) {
	this.corpId = corpId;
}
 
 
}
