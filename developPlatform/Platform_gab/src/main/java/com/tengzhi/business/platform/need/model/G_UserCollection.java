package com.tengzhi.business.platform.need.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.util.Date;
/**
 * 用户收藏表
 * @author lsh
 *
 */
@Entity
@Table(name = "g_user_collection")
public class G_UserCollection {
private String collectNote;
private String  userId;
private String  collectType;
private String  name;
private String  collectMode;
private String  contactMode;
private String  remarks;
private Date  collectTime;
private  String collectUrl;

@Id
public String getCollectNote() {
	return collectNote;
}
public void setCollectNote(String collectNote) {
	this.collectNote = collectNote;
}
public String getUserId() {
	return userId;
}
public void setUserId(String userId) {
	this.userId = userId;
}
public String getCollectType() {
	return collectType;
}
public void setCollectType(String collectType) {
	this.collectType = collectType;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getCollectMode() {
	return collectMode;
}
public void setCollectMode(String collectMode) {
	this.collectMode = collectMode;
}
public String getContactMode() {
	return contactMode;
}
public void setContactMode(String contactMode) {
	this.contactMode = contactMode;
}
public String getRemarks() {
	return remarks;
}
public void setRemarks(String remarks) {
	this.remarks = remarks;
}
public Date getCollectTime() {
	return collectTime;
}
public void setCollectTime(Date collectTime) {
	this.collectTime = collectTime;
}
public String getCollectUrl() {
	return collectUrl;
}
public void setCollectUrl(String collectUrl) {
	this.collectUrl = collectUrl;
}



}
