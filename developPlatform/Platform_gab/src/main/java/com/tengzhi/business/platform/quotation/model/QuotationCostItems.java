package com.tengzhi.business.platform.quotation.model;


import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;
import java.math.BigDecimal;

@Entity
@Table(name = "g_quatation_cost_items")
public class QuotationCostItems {
    private String id,directCostItems,costItems,company,remarks;
    private int count;
    private BigDecimal unitPrice,money;

    @Id
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDirectCostItems() {
        return directCostItems;
    }

    public void setDirectCostItems(String directCostItems) {
        this.directCostItems = directCostItems;
    }

    public String getCostItems() {
        return costItems;
    }

    public void setCostItems(String costItems) {
        this.costItems = costItems;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getRemarks() {
        return remarks;
    }

    public void setRemarks(String remarks) {
        this.remarks = remarks;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public BigDecimal getUnitPrice() {
        return unitPrice;
    }

    public void setUnitPrice(BigDecimal unitPrice) {
        this.unitPrice = unitPrice;
    }

    public BigDecimal getMoney() {
        return money;
    }

    public void setMoney(BigDecimal money) {
        this.money = money;
    }
}
